public class Position {
    public int x;
    public int y;
    public int z;

    /**
     * Constructeur par defaut de l'objet "Position"
     * @param x Position x de la EKOConsole
     * @param y Position y de la EKOConsole
     * @param z Position z de la EKOConsole
     */
    public Position(int x, int y, int z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
